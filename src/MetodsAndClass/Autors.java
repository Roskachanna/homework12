package MetodsAndClass;

public class Autors {

        private final String name;
        private final String surname;


        public Autors(String name, String surname){
            this.name = name;
            this.surname = surname;
        }
    public String getName() {
        return this.name;
    }
    public String getSurname() {
        return this.surname;
    }

    }

